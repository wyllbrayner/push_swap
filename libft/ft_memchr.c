#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t len)
{
	unsigned char	*ustr;
	size_t			i;

	ustr = (unsigned char *)str;
	i = 0;
	while (i < len)
	{
		if (ustr[i] == (unsigned char)c)
			return ((char *)&ustr[i]);
		i++;
	}
	return (NULL);
}
