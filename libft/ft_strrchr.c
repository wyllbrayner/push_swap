#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	long long int	len;

	len = (long long int)ft_strlen(str);
	while (len >= 0)
	{
		if (str[len] == (unsigned char)c)
			return ((char *)&str[len]);
		len--;
	}
	return (NULL);
}
